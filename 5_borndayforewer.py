while True:
        age = int(input('Введите год рождения А.С. Пушкина: '))
        if age == 1799:
            break
        print('Вы ввели неверно, попробуйте еще раз.')
print('Вы ввели верно, год рождения А.С. Пушкина', age, 'г.')

